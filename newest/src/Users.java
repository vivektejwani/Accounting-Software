    private void add_final_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_final_btnActionPerformed
        // TODO add your handling code here:
        try
        {
            if(new_user_text.getText().equalsIgnoreCase("admin"))
            {
                JOptionPane.showMessageDialog(null, "Cannot add another admin!", "Warning", 1);
            }
            else
            {
            while(rs.next())
            {}
            rs.previous();
            rs.moveToInsertRow();
            rs.updateString("username",new_user_text.getText());
            rs.updateString("password", new_pass_text.getText());
            rs.updateDouble("late_amt", StartPage.late_amt);
            rs.insertRow();
            stmt.close();
            rs.close();
            stmt=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            rs=stmt.executeQuery(sql);
            rs.next();
            JOptionPane.showMessageDialog(null, "User Added!", "Alert", 1);
            add_panel.setVisible(false);
            main_panel.setVisible(true);
            }
        }
        catch(SQLException err)
        {
            JOptionPane.showMessageDialog(null, err.getMessage(), "Error!", 1);
        }
        
    }//GEN-LAST:event_add_final_btnActionPerformed

