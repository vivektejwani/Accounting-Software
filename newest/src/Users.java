    private void delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_btnActionPerformed
        // TODO add your handling code here:
        if(admin)
        {
            
            delete_user_btn.setEnabled(false);
            delete_panel.setVisible(true);
            main_panel.setVisible(false);
        }
        else
        {
            if(JOptionPane.showConfirmDialog(null, "Delete "+user+" ?", "Confirmation!", JOptionPane.OK_CANCEL_OPTION) == 0)
            {
                try
                {
                    rs.deleteRow();
                    stmt.close();
                    rs.close();
                    JOptionPane.showMessageDialog(null,"Deleted!",null,1);
                    this.dispose();
                    new StartPage().setVisible(true);
                }
                catch(SQLException err)
                {
                    JOptionPane.showMessageDialog(null, err.getMessage(), "Error!", 1);
                }  
            }
        }
    }//GEN-LAST:event_delete_btnActionPerformed

