    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        // TODO add your handling code here:
        try
        {    
            this.late_amt=Double.parseDouble(late_penalty_text.getText());
            String sql="select * from users";
            rs=stmt.executeQuery(sql);
            while(rs.next())
            {
                rs.updateDouble("late_amt", late_amt);
                rs.updateRow();
            }
            JOptionPane.showMessageDialog(null, "Saved", null, 1);
            settings_panel.setVisible(false);
            start_panel.setVisible(true);
        }
        catch(SQLException err)
        {
            JOptionPane.showMessageDialog(null,err.getMessage(),"Error", 1);
        }
    }//GEN-LAST:event_save_btnActionPerformed

