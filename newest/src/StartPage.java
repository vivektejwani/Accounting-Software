    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        try
        {
            
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/College?zeroDateTimeBehavior=convertToNull", username, pass);
            stmt=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String user=user_text.getText();
            char[] pass=pass_text.getPassword();
            String sql = "select * from users where username = '"+user+"'";
            rs=stmt.executeQuery(sql);
            if(rs.next())
            {   
                this.id= rs.getInt("id");
                char[] correct=rs.getString("password").toCharArray();
                if(Arrays.equals(pass, correct))
                {
                    invalid_label.setVisible(false);
                    new_btn.setVisible(true);
                    old_btn.setVisible(true);
                    users_btn.setVisible(true);
                    new_courses_btn.setVisible(true);
                    new_courses_btn.setEnabled(false);
                    old_courses_btn.setVisible(true);
                    old_courses_btn.setEnabled(false);
                    settings_btn.setVisible(true);
                    settings_btn.setEnabled(false);
                    sql = "select * from users where username = '"+user+"'";
                    rs=stmt.executeQuery(sql);
                    rs.next();
                    late_amt=rs.getDouble("late_amt");
                    if(user.equalsIgnoreCase("admin"))
                    {
                        new_courses_btn.setEnabled(true);
                        old_courses_btn.setEnabled(true);
                        settings_btn.setEnabled(true);
                    }
                 
                }
                else
                {
                    invalid_label.setVisible(true);
                    new_btn.setVisible(false);
                    old_btn.setVisible(false);
                    users_btn.setVisible(false);
                    new_courses_btn.setVisible(false);
                    old_courses_btn.setVisible(false);
                    settings_btn.setVisible(false);
                }
            }
            else
            {
                     invalid_label.setVisible(true);
                     new_btn.setVisible(false);
                     old_btn.setVisible(false);
                    users_btn.setVisible(false);
                    new_courses_btn.setVisible(false);
                    old_courses_btn.setVisible(false);
                    settings_btn.setVisible(false);
            }
        }
        catch(SQLException err)
        {
            JOptionPane.showMessageDialog(null, err.getMessage(), "Error!", 1);
        }
    }//GEN-LAST:event_submit_btnActionPerformed

