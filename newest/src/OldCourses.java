    private void find_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_find_btnActionPerformed
        // TODO add your handling code here:
        try
        {
            String sql = "select id as 'ID',course_name as 'Course Name' from courses where course_name like '%"+course_name_text.getText()+"%'";
            rs=stmt.executeQuery(sql);
            course_table.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(SQLException err)
        {
            JOptionPane.showMessageDialog(null, err.getMessage(), "Error!", 1);
        }
        
    }//GEN-LAST:event_find_btnActionPerformed

    private void course_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_course_tableMouseClicked
        // TODO add your handling code here:
        int row = course_table.rowAtPoint(evt.getPoint());
        course_table.setRowSelectionInterval(row, row);
        name = (String)course_table.getModel().getValueAt(row, 1);
        id = (Integer)course_table.getModel().getValueAt(row, 0);
        select_btn.setEnabled(true);
    }//GEN-LAST:event_course_tableMouseClicked

