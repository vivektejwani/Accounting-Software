    private void delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_btnActionPerformed
        // TODO add your handling code here:
        
        try
        {
            if(JOptionPane.showConfirmDialog(null, "Delete ?", "Confirmation!", JOptionPane.OK_CANCEL_OPTION) == 0)
            {
            String sql = "Select * from courses where id = "+id;
            rs=stmt.executeQuery(sql);
            rs.next();
            rs.deleteRow();
            rs.close();
            stmt.close();
            JOptionPane.showMessageDialog(null, "Deleted!", "Alert", 1);
            this.dispose();
            new OldCourses().setVisible(true);
            }
        }
        catch(SQLException err)
        {
            JOptionPane.showMessageDialog(null, err.getMessage(), "Error!",1);
        }
        
    }//GEN-LAST:event_delete_btnActionPerformed

